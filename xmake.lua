package("auxtypes")

    set_homepage("http://github.com/abdielsouza/auxtypes.git")
    set_description("Auxiliary resources for C++ projects.")

    set_urls("http://github.com/abdielsouza/auxtypes.git")

    add_versions("0.1.0", "8d7e9f698ce48787b6e1c67e6bff79e487303e66077e25cb9784ac8835978017")

    on_install("windows", function (package)
        io.gsub("win32/Makefile.msc", "%-MD", "-" .. package:config("vs_runtime"))
        os.vrun("nmake -f win32\\Makefile.msc auxtypes.lib")
        os.cp("auxtypes.lib", package:installdir("lib"))
        os.cp("*.h", package:installdir("include"))
    end)

    on_install("linux", "macosx", function (package)
        import("package.tools.autoconf").install(package, {"--static"})
    end)
 
    on_install("iphoneos", "android@linux,macosx", "mingw@linux,macosx", function (package)
        import("package.tools.autoconf").configure(package, {host = "", "--static"})
        io.gsub("Makefile", "\nAR=.-\n",      "\nAR=" .. (package:build_getenv("ar") or "") .. "\n")
        io.gsub("Makefile", "\nARFLAGS=.-\n", "\nARFLAGS=cr\n")
        io.gsub("Makefile", "\nRANLIB=.-\n",  "\nRANLIB=\n")
        os.vrun("make install -j4")
    end)

package_end()

add_rules("mode.debug", "mode.release")

rule("test")
    set_extensions(".cpp") -- Associate the rule with `.txt` files
    on_build(function (target)
        print("Building target: " .. target:name())
    end)
    on_run(function (target)
        print("Running tests for target: " .. target:name())
    end)

target("auxtypes")
    set_kind("headeronly")
    set_languages("c99", "cxx17")
    add_includedirs("include", {public=true})

target("tests")
    set_kind("binary")
    add_deps("auxtypes")
    add_files("tests/*.cpp")
    set_group("test") -- organiza no grupo de testes
    add_rules("test") -- habilita execução via `xmake test`

